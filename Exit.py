import atexit


class Exit:

    ############################################################################
    def __init__(self):
        self.functions = []

    ############################################################################
    def register(self, function):
        if function is None:
            return
        self.functions.append(function)
        atexit.register(function)

    ############################################################################
    def exit(self):
        for function in self.functions:
            function()


################################################################################
################################################################################
################################################################################
exit = Exit()
